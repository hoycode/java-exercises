/* Exercise 7

Write a program in Java to display the first 5 natural numbers                        

*/

public class NaturalNumbers {
    public static void main(String[] args) {

        System.out.println("The first 5 natural numbers are: \n");
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }
}
